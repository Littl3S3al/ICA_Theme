                    <h1>Simple Spinal alignment</h1>
                    <p>
                        This first sequence regulates the ventral vagus by repositioning the first two neck vertebrae – this increases bloodflow and has a positive effect on the ventral vagal system. It is interesting to note that something as slight as a fearful thought can put these two vertebrae out of alignment – compromising function all the way through the ventral vagal system – face, throat, lung, heart, diaphragm. 
                    </p>
                    <ul>
                        <li>Lie in constructive rest position. </li>
                        <li>First observe the motion of your neck from side to side. Moving your head first to the right. Chin to shoulder and then to the left. Take note of your range of motion.</li>
                        <li>Interweave your fingers and place them behind your head. Feel the hardness of your cranium with your fingers. Feel the bones of your fingers on the back of your head. </li>
                        <li>Without moving your head. Look to the right as far as you can. Keep looking until you feel a swallow, a yawn or a sigh. This is a sign that your autonomic nervous system is relaxing. </li>
                        <li>Look straight ahead. </li>
                        <li>Now repeat on the left. Without moving your head. Look to the left as far as you can. </li>
                        <li>Keep looking until you feel a swallow, a yawn or a sigh.</li>
                        <li>Now observe the motion of your neck from side to side. Has your range of motion increased?</li>
                    </ul>
