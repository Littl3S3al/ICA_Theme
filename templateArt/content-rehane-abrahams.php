                    <h1>Exercise for Ventral Vagal innervation in your chest</h1>
                    <p>We are working on the vagus nerve as it goes into the pace maker of our heart, our lung tissue and the fascial tissue of the diaphragm. There are three stages to this exercise: on the back; the side and the belly.</p>

                    <h2>On the Back</h2>
                    <ul>
                        <li>Lying in constructive rest position. Take a pillow rolled up and place it under your hips or pelvis so that your pelvis is higher than your chest and neck. This produces a response in your carotid artery that encourages ventral vagal relaxation.</li>
                        <li>Inhale until you are full. Once you are full – swallow. Just simulate a swallowing action. </li>
                        <li>Exhale for longer than your inhale. </li>
                        <li>Then hold out the breath or wait until you have to breathe. </li>
                        <li>Repeat this exercise as many times as you like. Inhale till you are full. Swallow. Exhale for longer than your inhale. Then wait until you need to breathe. Do as many rounds as you like.</li>
                    </ul>

                    <h2>On the Side</h2>
                    <ul>
                        <li>Lie on your side – with a pillow under your chest. Your head on another pillow or block. </li>
                        <li>Be comfortable and at ease.</li>
                        <li>Put your upper hand on your ribs. Breathe in to full. Make the action as if you were going to cough – you don’t need to actually cough. This action will also relax your diaphragm. </li>
                        <li>Breathe out and hold the breath out for as long as you can. </li>
                        <li>Now try that on the other side. Lie on your side – with a pillow under your chest. Your head on another pillow or block. Put your upper hand on your ribs. Breathe in to full. Make the action as if you were going to cough –. Breathe out and hold the breath out for as long as you can. </li>
                    </ul>

                    <h2>On the Belly</h2>
                    <ul>
                        <li>Now lie on your belly – with the pillow rolled up under your sternum.</li>
                        <li>Breathe in to full. </li>
                        <li>Make the action as if you were going to cough.</li>
                        <li>Breathe out and hold the breath out for as long as you can. </li>
                        <li>Repeat as many rounds as you need.</li>
                    </ul>
