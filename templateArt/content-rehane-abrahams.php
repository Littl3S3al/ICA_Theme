                    <h1>Massage</h1>
                    <h2>Fascial Massage of the Pelvis (additional to the video)</h2>
                    <p>
                        The fascial web interconnects all tissue. Particularly responsive to threat and GBV is the Psoas Muscle which connects the legs to the torso. Survivors of Sexual Violence are noted to suffer with extremely constricted Psoas Muscles. Stress in the musculature and fascia effect the organs and can lead to long term issues in the reproductive or other gut organs. For eg. BIPOC women in SA have unusually high incidences of Uterine Fibroids.
                    </p>
                    <ul>
                        <li>Lie on your back in the constructive rest position with your knees up. Breathe fully in and out through the nose. </li>
                        <li>With three fingers of your dominant hand move your hand over your belly feeling into the fascia just below your layer of fat.</li>
                        <li> Begin to feel in the fascia for knots. Use your other hand to assist. When you have found a knot massage gently for about 10 or 20 seconds – you may feel the knot break up. </li>
                        <li>Move your hand around your belly – gently massaging. </li>
                        <li>Allow sound to fall out of you. Remain relaxed. </li>
                        <li>Avoid your belly button it is very full of nerve endings and very sensitive – do not massage there. Move your hands gently around your belly locating and massaging more spots. Breathe fully in and out. </li>
                    </ul>

                    <p>If what you’re doing hurts, back off. This should feel slightly uncomfortable, and if your belly isn’t relaxed it might be more uncomfortable than it should be, but it shouldn’t HURT.</p>
                    <p>If you find you have an extremely tight/knotted up belly, then try this once a day for a week.</p>
